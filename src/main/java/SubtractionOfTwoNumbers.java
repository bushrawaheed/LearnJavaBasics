public class SubtractionOfTwoNumbers {

    public static void operation(int a, int b) {
        System.out.println("a - b, a + b and a * b is ");
        // sub
        System.out.println(a - b);
        System.out.println(a + b);
        System.out.println(a * b);
    }

    public static void main(String args[]) {

        SubtractionOfTwoNumbers.operation(3,5);

        SubtractionOfTwoNumbers.operation(9,3);

        SubtractionOfTwoNumbers.operation(10, 20);


    }

    }
